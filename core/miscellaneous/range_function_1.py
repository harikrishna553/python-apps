print("Iterate from 0 to some maximum number")
for i in range(6):
    print(i)

print("\nIterate from some number to other number")
for i in range(2, 6):
    print(i)

print("\nChange the step size to 3")
for i in range(2, 6, 3):
    print(i)

print("\nDecrementing the step size")
for i in range(6, 2, -1):
    print(i)